    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        
        PharmacyRequest patTransfer = new PharmacyRequest();
        patTransfer.setSender(account);
        
        //Get the ambulance driver user account
        PharmacyOrganization ambOrg = (PharmacyOrganization)comboPharmacy.getSelectedItem();
        UserAccount pharm = ambOrg.getUserAccountDirectory().getUserByRole(Role.RoleType.Pharmacist);
        patTransfer.setReceiver(pharm);
        patTransfer.setSender(account);
       
        //patTransfer.setPatient(selectPatient);
        patTransfer.setMessage((wq).getMessage());
        
        //Set time
//        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
//        LocalDateTime now = LocalDateTime.now();  
        //patTransfer.setTime(dtf.format(now));
        
        //Add to works queues
        pharm.getWorkQueue().getWorkRequestList().add(patTransfer);
        account.getWorkQueue().getWorkRequestList().add(patTransfer);
        
        patTransfer.setStatus("Requested Pharmacy");
        JOptionPane.showMessageDialog(this, "Pharmacy request raised");
        populateWorkRequests();
        
    }//GEN-LAST:event_btnRequestActionPerformed

    public void populateWorkRequests(){
        DefaultTableModel model = (DefaultTableModel) tblPharmacy.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest wq : account.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[3];
            if(wq.getStatus().equals("Requested Pharmacy")){
            row[0] = (PharmacyOrganization)comboPharmacy.getSelectedItem();
            row[1] = wq.getMessage();
            row[2] = wq.getStatus();
            }
            
            model.addRow(row);
        }
    }
    

