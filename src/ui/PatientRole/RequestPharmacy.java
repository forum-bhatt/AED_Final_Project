    public void populatePharmacys(){
        comboPharmacy.removeAllItems();
        
        String networkName = account.getEmployee().getAddress();
        
        for(Enterprise ent:business.getNetwork(networkName).getEnterpriseDirectory().getEnterpriseList()){
            if(!ent.getEnterpriseType().toString().contains("Pharmacy")){
                continue;
            }
            for(Organization org: ent.getOrganizationDirectory().getOrganizations(Organization.Type.Pharmacy)){
                comboPharmacy.addItem(org);
            }
        }      
    }
