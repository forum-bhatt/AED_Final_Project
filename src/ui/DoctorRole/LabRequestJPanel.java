    public void populateLabs(){
        cmbLabs.removeAllItems();
        
        for(Enterprise ent:business.getNetwork("Boston").getEnterpriseDirectory().getEnterpriseList()){
            
            if(!ent.getEnterpriseType().toString().equals("Lab")){
                continue;
            }
            for(Organization org:ent.getOrganizationDirectory().getOrganizationList()){
                if(!org.getType().toString().contains("Lab")){
                    continue;
                }
                cmbLabs.addItem(org);
            }
            
        }
    }
