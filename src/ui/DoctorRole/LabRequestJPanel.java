    private void btnRequestTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestTestActionPerformed
        // TODO add your handling code here:
        
        LabOrganization labOrg = (LabOrganization)cmbLabs.getSelectedItem();
        UserAccount labAss = labOrg.getUserAccountDirectory().getUserByRole(Role.RoleType.LabAssistant);
        
        LabRequest labReq = new LabRequest();
        labReq.setDoctorRequest((DoctorRequest)wq);
        labReq.setReceiver(labAss);
        labReq.setSender((wq).getSender());
        labReq.setMessage(txtTest.getText());
        labReq.setStatus("Assigned to lab");
        //.getWorkQueue().getWorkRequestList().add(labReq);
        (wq).getSender().getWorkQueue().getWorkRequestList().add(labReq);
        labAss.getWorkQueue().getWorkRequestList().add(labReq);
        JOptionPane.showMessageDialog(this, "Lab Request Sent");
    }//GEN-LAST:event_btnRequestTestActionPerformed


